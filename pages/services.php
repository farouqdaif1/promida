<script>
    const services=  document.querySelector('.all-services-small');
    services.addEventListener('click', (e) => {
        let list=[...e.target.classList]
        const articles = document.querySelectorAll('article');
        let clickedServices=[]
        if (list.includes("branding-e")) {
            clickedServices.push("branding")
        }else if (list.includes("website-e") ) {
            clickedServices.push("website")
        }else if (list.includes("social-e")) {
            clickedServices.push("social")
        }else if (list.includes("production-e")) {
            clickedServices.push("production")
        }else if (list.includes("media-e")) {
            clickedServices.push("media")
        }else if (list.includes("strategies-e")) {
            clickedServices.push("strategies")
        }else {
            clickedServices.push("branding")
        }
        const clickedService = document.getElementsByClassName("card-small");
        articles.forEach((element,index)=>{
            let classes=[...element.classList]
            if (classes.includes(clickedServices[0])){
                element.classList.add('article');
                document.getElementById(clickedServices[0]).classList.add('card-active') 
            }else{
                element.classList.remove('article')
                clickedService[index].classList.remove('card-active')
            }
        })
    });
    let loader=document.getElementById("preloader");
    window.addEventListener('load',function(){
        loader.style.display="none"
    })
</script>
